# List Slicing
my_list = [10, 20, 30, 40, 50, 60, 70, 80]

print("Original List:", my_list)
print("Slice [1:5]:", my_list[1:5])  # Elements from index 1 to 4
print("Slice [:4]:", my_list[:4])    # First 4 elements
print("Slice [3:]:", my_list[3:])    # Elements from index 3 to end
print("Slice [-4:-1]:", my_list[-4:-1])  # Last four elements except last one
print("Slice [::2]:", my_list[::2])  # Every second element
print("Slice [::-1]:", my_list[::-1]) # Reverse the list
